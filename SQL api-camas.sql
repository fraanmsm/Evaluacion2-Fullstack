BEGIN
   EXECUTE IMMEDIATE 'DROP TABLE CAMAS CASCADE CONSTRAINTS';
EXCEPTION
   WHEN OTHERS THEN NULL;
END;
/

CREATE TABLE CAMAS (
    ID                  NUMBER(19, 0) GENERATED BY DEFAULT AS IDENTITY,
    NUMERO_CAMA         VARCHAR2(50) NOT NULL,
    PABELLON_GRAVEDAD   NUMBER(10, 0) NOT NULL,
    ESTADO              VARCHAR2(50) NOT NULL,
    
    CONSTRAINT PK_CAMAS PRIMARY KEY (ID),
    CONSTRAINT CHK_CAMAS_GRAVEDAD CHECK (PABELLON_GRAVEDAD BETWEEN 1 AND 5)
);

COMMENT ON TABLE CAMAS IS 'Inventario de camas para validación del servicio.';
COMMIT;